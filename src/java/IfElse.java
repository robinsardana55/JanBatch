package java;

public class IfElse {
    public static void main(String[] args) {
        System.out.println("Enter any number ");
        int x = 11;

        if ( x >= 1 && x <= 10) {
            System.out.println("The value of x is between 1 and 10 both inclusive");
        } else {
            System.out.println("The value of x is not between 1 and 10" + x);

        }
    }
}
